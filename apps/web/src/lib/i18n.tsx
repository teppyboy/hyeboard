export type Translations = typeof en;

const dictionaries: Record<Locale, Translations> = { en, vi: vi() };

type LocaleState = { locale: Locale; setLocale: (locale: Locale) => void; t: Translations };
const LocaleContext = createContext<LocaleState | null>(null);

function detectInitialLocale(): Locale {
  const storedLocale = localStorage.getItem(LOCALE_STORAGE_KEY);
  if (storedLocale === "en" || storedLocale === "vi") return storedLocale;
  return navigator.language?.toLowerCase().startsWith("vi") ? "vi" : "en";
}

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => detectInitialLocale());

  useEffect(() => {
    document.documentElement.lang = locale;
    localStorage.setItem(LOCALE_STORAGE_KEY, locale);
  }, [locale]);

  const setLocale = (next: Locale) => setLocaleState(next);

  return (
    <LocaleContext.Provider value={{ locale, setLocale, t: dictionaries[locale] }}>
      {children}
    </LocaleContext.Provider>
  );
}

export function useLocale(): LocaleState {
  const state = useContext(LocaleContext);
  if (!state) throw new Error("useLocale must be used inside LocaleProvider");
  return state;
}
